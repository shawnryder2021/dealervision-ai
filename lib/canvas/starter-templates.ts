function brand(d: Dealership | null) {
  return {
    primary: d?.brand_colors?.primary || "#0F2A47",
    secondary: d?.brand_colors?.secondary || "#FFFFFF",
    accent: d?.brand_colors?.accent || "#FF6B00",
  };
}

