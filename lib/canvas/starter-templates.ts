export function templateToDesign(t: StarterTemplate, dealership: Dealership | null): Partial<Design> {
  return {
    name: t.label,
    kind: "draft",
    canvasSize: t.canvasSize,
    canvasWidth: t.canvasWidth,
    canvasHeight: t.canvasHeight,
    backgroundColor: t.backgroundColor,
    elements: t.build(dealership),
  };
}
