/** Year range for vehicle selection */
export function getYearRange(): number[] {
  const currentYear = new Date().getFullYear();
  const years: number[] = [];
  for (let y = currentYear + 1; y >= currentYear - 10; y--) {
    years.push(y);
  }
  return years;
}
